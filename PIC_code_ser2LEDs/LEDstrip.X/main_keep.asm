	; Move up all data.
	banksel 0
	movlw low bufferEnd
	movwf FSR1L
	movlw high bufferEnd
	movwf FSR1H
	movlw low (bufferEnd-2)
	movwf FSR0L
	movlw high (bufferEnd-2)
	movwf FSR0H
