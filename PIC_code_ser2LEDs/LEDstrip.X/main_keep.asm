	moviw --FSR0
	movwi --FSR1
	moviw --FSR0
	movwi --FSR1
	movf FSR0L,0
	btfss STATUS,Z
	bra copyLoop
	movf FSR0H,0
	xorlw high buffer
	btfss STATUS,Z
	bra copyLoop
	; done with move.

