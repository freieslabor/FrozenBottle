	banksel 0
	movlw low buffer
	movwf FSR1L
	movlw high buffer
	movwf FSR1H

	; insert new stuff.
	movf ml_ledSequenceCount,0
	movwf ml_temp
	clrf ml_in16bitL
	clrf ml_in16bitH
	movlw high sequenceR
	movwf PCLATH
